import re

def is_palindrome(text: str) -> bool:
    """
    Проверяет, является ли строка палиндромом, игнорируя регистр и пробелы.

    Аргументы:
        text (str): Строка для проверки.

    Возвращает:
        bool: True, если строка является палиндромом, иначе False.
    """
    # 1. Приводим строку к нижнему регистру
    # 2. Находим все буквенно-цифровые символы (игнорируем пробелы и знаки препинания)
    # 3. Соединяем их в одну "очищенную" строку
    cleaned_text = "".join(re.findall(r'[а-яa-z0-9]', text.lower()))
    
    # Сравниваем очищенную строку с её перевёрнутой версией
    return cleaned_text == cleaned_text[::-1]
  
